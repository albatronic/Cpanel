/**
 * @orm:Entity(ErpMarcas)
 */
class ErpMarcas extends ErpMarcasEntity {

    public function __toString() {
        return $this->getTitulo();
    }

    public function fetchAll($column = '', $default = TRUE) {
        if ($column == '')
            $column = 'Titulo';
        return parent::fetchAll($column, $default);
    }
}
